package com.mycompany.app;


public class Calculadora {

    

    public long sumar(long numero1, long numero2) {
        return numero1 + numero2;
    }

    public long restar(long numero1, long numero2) {
        return numero1 - numero2;
    }

    public long multiplicar(long numero1, long numero2) {
        return numero1 * numero2;
    }

    public float dividir(long numero1, long numero2) {
        return numero1 / numero2;
    }

}
